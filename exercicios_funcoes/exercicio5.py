def tabuada_dois():
    numero = 2
    for i in range (1,11):
        n = numero * i
        print(f"2 x {i} = {n}")

def tabuada_tres():
    numero = 3
    for i in range (1,11):
        n = numero * i
        print(f"3 x {i} = {n}")

def tabuada_quatro():
    numero = 4
    for i in range (1,11):
        n = numero * i
        print(f"4 x {i} = {n}")

tabuada_dois()
print()
